n = (input('Digite um valor: '))
print (n, 'detalhado:',"\n"
       'Alpha Numérico:', n.isalnum(), "\n"
       'Decimal:', n.isdecimal(), "\n"
       'É do Alfabeto:', n.isalpha(), "\n" 
       'É Espaço:', n.isspace(), "\n" 
       'É maiuscula:', n.isupper(), "\n"
       'É capitalizado:', n.istitle(), "\n"
       'Type:',  (type (n)))


